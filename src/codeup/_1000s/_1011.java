package codeup._1000s;

import java.util.Scanner;

public class _1011 {
	// main : ctrl + space (자동완성)
	public static void main(String[] args) {
		// 1. 문자를 입력
		Scanner sc = new Scanner(System.in);
		
		// 2. 문자를 변수에 대입
		// 변수 선언 - 문자 데이터 타입 char
		String c = "";
		c = sc.next();
		
		// 3. 변수를 출력
		// sysout : ctrl + space (자동완성
		System.out.println(c);
		
		sc.close();
	}
}
